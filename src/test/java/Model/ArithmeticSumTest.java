    void assertionSumValid(BigDecimal x, BigDecimal y, BigDecimal sum) {
        sum = scaleForBigDecimal(sum);
        assertEquals(sum, Arithmetic.sum(x, y));
        assertEquals(sum, scaleForBigDecimal(x.add(y, new MathContext(16))));
    }

    void combinationSumValidNegative(BigDecimal x, BigDecimal y, String sumXY, String minusXY) {
        BigDecimal xNegative = x.negate();
        BigDecimal yNegative = y.negate();
        String sumXYNegative = "-" + sumXY;
        String minusXYNegative = "-" + minusXY;


        if(x.compareTo(y) < 0 || x.compareTo(y) == 0) {
            combinationsSumValidMovePoint(x, y, sumXY);
            combinationsSumValidMovePoint(xNegative, yNegative, sumXYNegative);

            combinationsSumValidMovePoint(x, yNegative, minusXYNegative);
            combinationsSumValidMovePoint(xNegative, y, minusXY);
        } else {
            combinationsSumValidMovePoint(x, y, sumXY);
            combinationsSumValidMovePoint(xNegative, yNegative, sumXYNegative);

            combinationsSumValidMovePoint(x, yNegative, minusXY);
            combinationsSumValidMovePoint(xNegative, y, minusXYNegative);
        }
    }

