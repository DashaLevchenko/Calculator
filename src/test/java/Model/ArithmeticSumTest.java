    void pointMovement(BigDecimal x, BigDecimal y, BigDecimal sum, int point) {
        x = x.movePointLeft(point);
        y = y.movePointLeft(point);
        sum = sum.movePointLeft(point);

        assertionSum(x, y, sum);
    }

    static BigDecimal scaleForBigDecimal(BigDecimal numberDouble) {
        numberDouble = numberDouble.stripTrailingZeros();
        if (numberDouble.scale() < 0) {
            numberDouble = numberDouble.setScale(0);
        }
        return numberDouble;
    }
