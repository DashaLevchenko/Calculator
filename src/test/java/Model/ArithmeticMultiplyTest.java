    void assertionMultiplyValid(BigDecimal x, BigDecimal y, BigDecimal multiply) {
        BigDecimal differenceActual = Arithmetic.multiply(x, y);
        multiply = scaleForBigdecimal(multiply.stripTrailingZeros());
        assertEquals(multiply, differenceActual);
        assertEquals(multiply, scaleForBigdecimal(x.multiply(y).stripTrailingZeros()));

    }

    void combinationMultiplyValidNegative(BigDecimal x, BigDecimal y, String multiplyXY) {
        BigDecimal xNegative = x.divide(valueOf(-1));
        BigDecimal yNegative = y.divide(valueOf(-1));
        String multiplyXYNegative = "-" + multiplyXY;


        combinationsMultiplyValidMovePoint(x, y, multiplyXY);
        combinationsMultiplyValidMovePoint(x, yNegative, multiplyXYNegative);

        combinationsMultiplyValidMovePoint(xNegative, y, multiplyXYNegative);
        combinationsMultiplyValidMovePoint(xNegative, yNegative, multiplyXY);
    }

