    void pointMovement(BigDecimal x, BigDecimal y, BigDecimal multiply, int point) {
        x = x.movePointLeft(point);
        y = y.movePointLeft(point);
        multiply = multiply.movePointLeft(point+point);

        assertionMultiplyValid(x, y, multiply);
        multiplyZero(x, y);
    }

    void  multiplyZero(BigDecimal x, BigDecimal y){
        assertionMultiplyValid(x, valueOf(0), valueOf(0));
        assertionMultiplyValid(valueOf(0), y, valueOf(0));
    }

    static BigDecimal scaleForBigdecimal(BigDecimal numberDouble) {
        if (numberDouble.scale() < 0) {
            numberDouble = numberDouble.setScale(0);
        }
        return numberDouble;
    }
