    void assertionMultiplyValid(BigDecimal x, BigDecimal y, BigDecimal multiply) {
        multiply = scaleForBigdecimal(multiply);
        assertEquals(multiply, Arithmetic.multiply(x, y));
        assertEquals(multiply, scaleForBigdecimal(x.multiply(y)));

    }

    void combinationMultiplyNegative(String xString, String yString, String multiplyXY) {
        BigDecimal x = new BigDecimal(xString);
        BigDecimal y = new BigDecimal(yString);
        
        BigDecimal xNegative = x.negate();
        BigDecimal yNegative = y.negate();
        String multiplyXYNegative = "-" + multiplyXY;
        
        combinationsMultiplyValidMovePoint(x, y, multiplyXY);
        combinationsMultiplyValidMovePoint(x, yNegative, multiplyXYNegative);

        combinationsMultiplyValidMovePoint(xNegative, y, multiplyXYNegative);
        combinationsMultiplyValidMovePoint(xNegative, yNegative, multiplyXY);
    }

    void combinationsMultiplyValidMovePoint(BigDecimal x, BigDecimal y, String multiply) {
        BigDecimal sumBig = new BigDecimal(multiply);

        pointMovement(x, y, sumBig, 0);
        pointMovement(x, y, sumBig, 1);
        pointMovement(x, y, sumBig, 2);
        pointMovement(x, y, sumBig, 3);
        pointMovement(x, y, sumBig, 4);
        pointMovement(x, y, sumBig, 5);
        pointMovement(x, y, sumBig, 6);
        pointMovement(x, y, sumBig, 7);
        pointMovement(x, y, sumBig, 8);
        pointMovement(x, y, sumBig, 9);
        pointMovement(x, y, sumBig, 10);
        pointMovement(x, y, sumBig, 11);
        pointMovement(x, y, sumBig, 12);
        pointMovement(x, y, sumBig, 13);
        pointMovement(x, y, sumBig, 14);
        pointMovement(x, y, sumBig, 15);
        pointMovement(x, y, sumBig, 16);
    }

    void pointMovement(BigDecimal x, BigDecimal y, BigDecimal multiply, int point) {
        x = x.movePointLeft(point);
        y = y.movePointLeft(point);
        multiply = multiply.movePointLeft(point + point);

        assertionMultiplyValid(x, y, multiply);
        multiplyZero(x, y);
    }

    void multiplyZero(BigDecimal x, BigDecimal y) {
        assertionMultiplyValid(x, BigDecimal.ZERO, BigDecimal.ZERO);
        assertionMultiplyValid(BigDecimal.ZERO, y, BigDecimal.ZERO);
    }

    static BigDecimal scaleForBigdecimal(BigDecimal numberDouble) {
        numberDouble = numberDouble.stripTrailingZeros();
        if (numberDouble.scale() < 0) {
            numberDouble = numberDouble.setScale(0);
        }
        return numberDouble;
    }
