    void assertionsPercent(BigDecimal x, BigDecimal percent, String result) {
        assertEquals(new BigDecimal(result),  Arithmetic.percent(x, percent));
        assertEquals(new BigDecimal(result), scaleForBigDecimal(x.multiply(percent.movePointLeft(2), MathContext.DECIMAL128)));
    }


        void combinationPercent(BigDecimal x, BigDecimal percent, String result) {
        BigDecimal xNegative = x.negate();
        BigDecimal percentNegative = percent.negate();
        String resultNegative = "-"+result;

        assertionsPercent(x, percent, result);
        assertionsPercent(xNegative, percent, resultNegative);
        assertionsPercent(x, percentNegative, resultNegative);
        assertionsPercent(xNegative, percentNegative, result);
    }




    static BigDecimal scaleForBigDecimal(BigDecimal numberDouble) {
        numberDouble = numberDouble.stripTrailingZeros();
        if (numberDouble.scale() < 0) {
            numberDouble = numberDouble.setScale(0);
        }
        return numberDouble;
    }
