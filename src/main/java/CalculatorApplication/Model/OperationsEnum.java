package CalculatorApplication.Model;

/**
 * Enum representing operation for calculator
 */
public enum OperationsEnum {
    /**
     * The singleton is subtraction
     */
    SUBTRACT,

    /**
     * The singleton is subtraction
     */
    ADD,

    /**
     * The singleton is multiplication
     */
    MULTIPLY ,

    /**
     * The singleton is division
     */
    DIVIDE ,

    /**
     * The singleton is percent of number
     */
    PERCENT ,

    /**
     * The singleton is square root
     */
    SQRT ,

    /**
     * The singleton is square number
     */
    SQR,

    /**
     * The singleton is one division number
     */
    ONE_DIVIDE_X,

    /**
     * The singleton is negate
     */
    NEGATE,

    /**
     * The singleton is equal
     */
    EQUAL
}
